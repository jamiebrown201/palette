ProductsCompanion _rug({
  required String id,
  required String name,
  required String brand,
  required double price,
  required String hex,
  required Undertone undertone,
  required List<ProductMaterial> materials,
  required List<ProductStyle> styles,
  required TextureFeel texture,
  required VisualWeight weight,
  required FinishSheen sheen,
  required RugSize size,
  String? secondaryHex,
}) => ProductsCompanion(
  id: Value(id),
  category: const Value(ProductCategory.rug),
  name: Value(name),
  brand: Value(brand),
  retailer: Value(brand),
  priceGbp: Value(price),
  affiliateUrl: Value(
    _retailerUrls[brand] ?? 'https://www.google.co.uk/search?q=$name+$brand',
  ),
  imageUrl: const Value(''),
  primaryColourHex: Value(hex),
  secondaryColourHex: Value(secondaryHex),
  undertone: Value(undertone),
  materials: Value(materials),
  styles: Value(styles),
  textureFeel: Value(texture),
  visualWeight: Value(weight),
  finishSheen: Value(sheen),
  rugSize: Value(size),
  renterSafe: const Value(true),
  available: const Value(true),
);

